CREATE OR REPLACE PROCEDURE DEMO (P_EID NUMBER)

IS

V_COMM NUMBER;

BEGIN	
		
	SELECT COMM INTO V_COMM FROM EMPLOYEE WHERE EID = P_EID;
		
		IF V_COMM IS NULL THEN
		
		UPDATE EMPLOYEE SET COMM = 1000 WHERE EID = P_EID;

		ELSE
		
		DBMS_OUTPUT.PUT_LINE ('U ALREADY GOT COMM');
		
		END IF;
END;

	
